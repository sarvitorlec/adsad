function setup() {
  createCanvas(800, 400); // холст
}

function draw() {
  background(135, 206, 250); // Синее небо

  // 1. Облако
  fill(255); // Белое облако
  noStroke();
  ellipse(150, 100, 100, 50); // Первая часть облака
  ellipse(200, 100, 100, 50); // Вторая часть облака
  ellipse(175, 115, 80, 50); // Нижняя часть облака

  // 2. Горы
  fill(150, 75, 0); // Коричневые горы
  noStroke();
  triangle(300, 300, 200, 400, 400, 400); // Первая гора
  triangle(450, 280, 350, 400, 550, 400); // Вторая гора
  triangle(600, 260, 500, 400, 700, 400); // Третья гора

  // 3. ящик
  fill(100, 100, 100); // Серая ящик
  stroke(0);
  strokeWeight(2);
  rect(650, 300, 50, 50); // Прямоугольная форма ящика
  line(650, 300, 700, 350); // Линия внутри ящика для детализации
  line(700, 300, 650, 350);

  // 4. Собираемый предмет (яблоко)
  fill(255, 0, 0); // Красное яблоко
  noStroke();
  ellipse(675, 280, 30, 30); // Яблоко
  fill(0, 100, 0); // Зеленая ветка
  rect(675, 265, 2, 15); // Ветка для яблока

  // 5. Дерево
  fill(139, 69, 19); // Коричневый ствол дерева
  noStroke();
  rect(400, 300, 20, 60); // Ствол дерева
  fill(34, 139, 34); // Зеленая крона дерева
  ellipse(410, 280, 80, 80); // Крона дерева

  // 6. Трава (фоновая зеленая полоса)
  fill(0, 150, 0); // Зеленая трава
  rect(0, 350, width, height - 350);

  // 7. Солнце
  fill(255, 255, 0); // Желтое солнце
  noStroke();
  ellipse(750, 50, 60, 60); // Солнце на небе
}