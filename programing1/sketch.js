function setup() {
  createCanvas(600, 400);
}

function draw() {
  background(135, 206, 235); // Голубое небо
  
  // Горы
  fill(139, 69, 19); // Коричневый цвет
  noStroke();
  triangle(0, 400, 200, 200, 400, 400);
  triangle(200, 400, 400, 250, 600, 400);
  
  // Земля
  fill(34, 139, 34); // Зеленый цвет
  rect(0, 300, 600, 100);
  
  // Яма
  fill(0); // Черный цвет
  rect(450, 300, 50, 50);
  
  // Дерево
  fill(139, 100, 19); // Коричневый ствол
  rect(150, 250, 20, 50);
  fill(0, 128, 0); // Зеленая крона
  ellipse(160, 230, 60, 60);
  
  // Монетка
  fill(255, 215, 0); // Золотой цвет
  ellipse(300, 280, 20, 20);
  
  // Облако
  fill(255);
  noStroke();
  ellipse(100, 100, 60, 40);
  ellipse(140, 100, 80, 50);
  ellipse(180, 100, 60, 40);
}